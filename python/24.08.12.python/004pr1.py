# 화면에 Hello World 문자열을 출력하라.
print('Hello World')

# 화면에 아래 문장을 출력하라
# Mary's consmetics
print("Mary's cosmetics")
# escape 문자
print('Mary\'s cosmetics')

# \는 이스케이프 합니다
print('\는 이스케이프합니다')

# '\t는 탭입니다' 출력
print('\\t는 탭입니다')   #\\는 escape를 다시 탈출하는 방식

# 화면에 아래 문장을 출력하라
# 신씨가 소리질렀다. "도둑이야!".
print('신씨가 소릴질렀다. "도둑이야!".')
print("신씨가 소릴질렀다. \"도둑이야!\".")

